void xenos_write32(int reg, uint32_t val)
{
	write32n(0xec800000 + reg, val);
//	printf("set reg %4x %08x, read %08x\n", reg, val, read32n(0x200ec800000 + reg));
}

uint32_t xenos_read32(int reg)
{
	return read32n(0xec800000 + reg);
}

